public class PlusOne {
    public static void main(String[] args) {
        int number = 0;
        System.out.println(number);
        ++number; // prefix
        System.out.println(number);
        number++; // postfix
        System.out.println(number);
        System.out.println(number++); // prints and then increments by 1
        System.out.println(number); // prints 3
        System.out.println(++number); // prints 4

        // while loops
        /*
        while(condition){
            statements;
        }
         */


    }
}